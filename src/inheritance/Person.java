package inheritance;

public class Person {
	 String name;
	    String surname;
	    int age;
	     
	       public void walk() {
	    	   System.out.println("Person can walk");
	       }
	       protected void talk() {
	    	   System.out.println("Person can talk");
	       }
	       private void run() {
	    	   System.out.println("Person can run");
	       }
}
